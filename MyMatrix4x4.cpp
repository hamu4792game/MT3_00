Matrix4x4 MyMatrix4x4::Transpose(const Matrix4x4& m) {

	for (int y = 0; y < num; y++) {
		for (int x = 0; x < num; x++) {
			result.m[y][x] = m.m[x][y];
		}
	}

	return result;
}
