MyMatrix4x4::MyMatrix4x4():num(4) {
	for (int y = 0; y < num; y++)
	{
		for (int x = 0; x < num; x++)
		{
			result.m[y][x] = 0.0f;
		}
	}
}

MyMatrix4x4::~MyMatrix4x4() {

}

