Matrix4x4 MyMatrix4x4::MakeIdentity4x4() {

	for (int y = 0; y < 4; y++) {
		for (int x = 0; x < 4; x++) {
			if (y < 1 && x < 1) {
				result.m[y][x] = 1.0f;
			}
			else if (y = x) {
				result.m[y][x] = 1.0f;
			}
			else {
				result.m[y][x] = 0.0f;
			}
		}
	}

	return result;
}